        <p style="text-align: justify">
        Fakultas Peternakan (Faterna) Universitas Andalas didirikan tanggal 1 September 1963, atas ketetapan Menteri Perguruan Tinggi dan Ilmu Pengetahuan (PTIP) dengan Surat Keputusan No. 125 tanggal 9 Oktober 1963 yang ditetapkan di Jakarta tanggal 9 Oktober 1963. Berdasarkan urutan pendiriannya Fakultas Peternakan merupakan fakultas keenam dari 15 (lima belas) Fakultas di lingkungan Universitas Andalas. Pendirian Faterna dilatarbelakangi oleh perkembangan ilmu-ilmu pertanian yang dibutuhkan dalam pembangunan Indonesia dan perluasan Universitas Andalas. Pada awal berdiri sampai dengan tahun 1991 Faterna berlokasi di Kampus Unand Air Tawar, Padang dan sejak Semester Ganjil 1991/1992 pindah ke Kampus Unand di Limau Manis, Padang.
        </p>
        <p style="text-align: justify">
        Tahun 1983, Menteri Pendidikan dan Kebudayaan dengan Surat Keputusan No. 0125/O/1983, menetapkan 2 (dua) jurusan di Faterna Universitas Andalas yaitu Jurusan Produksi Ternak serta Jurusan Nutrisi dan Makanan Ternak. Masing-masing jurusan mempunyai 1 (satu) program studi yaitu :
        </p>
        <ol>
