        <p style="text-align: justify">
        Sejak tahun ajaran 1996/1997 Faterna Universitas Andalas telah menerapkan kurikulum yang berlaku secara nasional yaitu Kurikulum Nasional Program Sarjana Ilmu-Ilmu Pertanian berdasarkan Surat Keputusan Menteri Pendidikan dan Kebudayaan RI No. 0311/U/1994. Revisi kurikulum berdasarkan perkembangan ilmu dan teknologi serta kebutuhan pasar kerja dilakukan secara periodik.
        </p>
        <p style="text-align: justify">
        Jurusan Produksi Ternak berkembang menjadi 3 (tiga) Program Studi Sejak Tahun 1999, yaitu dengan diberikan izin penyelenggaraan Program Studi Teknologi Hasil Ternak dan Program Studi Sosial Ekonomi Peternakan. Dengan demikian Faterna Unand memiliki 2 Jurusan dengan 4 Program Studi. Setiap jurusan dipimpin oleh seorang Ketua Jurusan dan dibantu oleh seorang Sekretaris Jurusan. Program Studi dipimpin oleh seorang Ketua Program Studi dan seorang Sekretaris Program Studi
        </p>
